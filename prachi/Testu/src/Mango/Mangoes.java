package Mango;

public class Mangoes {
	int quality;
	String type;
	int price;
	
	public Mangoes()
	{
		
	}
	public Mangoes(int quality,String type,int price)
	{
	this.quality=quality;
	this.type=type;
	this.price=price;
		}
		
	public int getQuality() {
		return quality;
	}
	public void setQuality(int quality) {
		this.quality = quality;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	@Override
	public String toString() {
		return "Mangoes [quality=" + quality + ", type=" + type + ", price=" + price + "]";
	}

}
